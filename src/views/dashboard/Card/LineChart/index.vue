<template>
    <!-- 容器 -->
    <div class="charts" ref="charts"></div>
</template>

<script>
// 引入echarts
import echarts from 'echarts';
export default {
    name: 'LineChart',
    mounted() {
        // 初始化echarts实例
        let LineCharts = echarts.init(this.$refs.charts);
        //配置数据
        LineCharts.setOption({
            xAxis: {
                // 隐藏x轴
                show: false,
                type: 'category',
            },
            yAxis: {
                show: false,
            },
            series: [
                {
                    type: 'line',
                    data: [10, 7, 33, 12, 48, 9,29,10,44],
                    // 设置曲线平滑
                    smooth:true,
                    // 设置拐点的样式
                    itemStyle: {
                        opacity: 0,
                    },
                    // 设置线条的样式
                    lineStyle: {
                        color: "purple",
                    },
                    // 填充颜色
                    areaStyle: {
                        color: {
                            type: 'linear',
                            x: 0,
                            y: 0,
                            x2: 0,
                            y2: 1,
                            colorStops: [{
                                offset: 0, color: 'purple' // 0% 处的颜色
                            }, {
                                offset: 1, color: '#fff' // 100% 处的颜色
                            }],
                            global: false // 缺省为 false
                        }
                    }
                }
            ],
            // 布局调试
            grid: {
                left: 0,
                top: 0,
                right: 0,
                bottom: 0,
            }
        })
    }
}
</script>

<style>
.charts {
    width: 100%;
    height: 100%;
}
</style>